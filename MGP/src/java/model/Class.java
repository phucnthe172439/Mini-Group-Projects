package model;

/**
 *
 * @author asus
 */
public class Class {
    private int id; 
    private String code; 
    private String name; 
    private Boolean status; 
    private Subject subject;

    public Class() {
    }

    public Class(int id, String code, String name, Boolean status, Subject subject) {
        this.id = id;
        this.code = code;
        this.name = name;
        this.status = status;
        this.subject = subject;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }

    public Subject getSubject() {
        return subject;
    }

    public void setSubject(Subject subject) {
        this.subject = subject;
    }

    
    
    
}
